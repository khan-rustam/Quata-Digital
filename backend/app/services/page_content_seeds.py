def seed_default_section_content(db: Session) -> int:
    """For each page in DEFAULTS, if the row exists and has zero sections,
    fill it with the default starter sections. Returns the number of
    pages filled.

    Critically: only fills empty rows. If the boss has already added
    sections (even one), this is a no-op for that row — boss edits win.
    Pages stay UNPUBLISHED so the boss reviews before going live.
    """
    filled = 0
    for slug, sections in DEFAULTS.items():
        page = db.query(PageContent).filter(PageContent.slug == slug).first()
        if page is None:
            # Page doesn't exist yet — let `seed_default_pages` create it
            # first; it'll be filled on the next boot.
            continue
        if page.sections and len(page.sections) > 0:
            continue
        try:
            page.sections = validate_sections(sections)
            filled += 1
        except Exception:  # noqa: BLE001
            # Defensive — bad seed payload should never silently pass.
            # In dev this raises during validate_sections; logging here
            # lets prod boot continue with the page empty if a section
            # type was renamed and a payload wasn't updated.
            continue
    if filled:
        db.commit()
    return filled
