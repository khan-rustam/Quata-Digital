"""Default section payloads for the marketing CMS.

When a `PageContent` row exists but has zero sections (newly seeded by
`seed_default_pages` on a fresh DB, or after the page-content table was
just created), we fill it with sensible starter sections matching the
current static React copy. This means:
  - On first sign-in, the boss opens "Marketing pages → Home" and sees
    real content they can edit, not a blank canvas.
  - After publishing, the public site renders from the DB and looks
    identical to the existing static fallback.
  - Existing rows with sections are never overwritten — boss edits win.

Pages stay UNPUBLISHED on first seed so the boss confirms each one before
it goes live. The static React fallback continues to serve until then.

Adding default content for a new page:
  1. Add an entry to `DEFAULTS` keyed by slug.
  2. The dict is a list of section dicts matching the schema in
     app/schemas/page_sections.py — IDs are auto-generated by Pydantic
     when the seed runs.
"""
